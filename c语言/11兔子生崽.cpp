/*兔子生崽
有一对兔子出生后第三个月起，每个月都会生一对兔子，以此重复，假如兔子不死，
每个月的兔子总数为多少（输出前40个月）*/

#include <stdio.h>

int main()
{
	int a1=0, a2=1, a3, n;
	
	  a3=1;    //第一个月要另算 
	  printf("%d\n",a3);

	for(n=2; n<=40; n++)
	{
		a3=a1+a2;       //斐波那契数列：1 1 2 3 5 8 ...下个月是上两个月之和 
		a1=a2;
		a2=a3;
		printf("%d\n",a3);
	}
	
	return 0;
 } 
 
